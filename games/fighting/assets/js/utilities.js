var screenSize = {
    getWidth : function() {
        var size = document.width;
        return size;
    },
    getHeight : function() {
        var size = document.height;
        return size;
    },
    getWidthCenter : function() {
        var size = document.width / 2;
        return size;
    },
    getHeightCenter : function() {
        var size = document.height / 2;
        return size;
    }
};